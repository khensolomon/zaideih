import fs from 'fs/promises';
import path from 'path';
import sharp from 'sharp';

// In-memory cache to prevent re-processing identical files + queries.
// Note: For massive projects, consider replacing this Map with an 'lru-cache'
// to prevent memory bloat during extremely long Vite dev server sessions.
const transformationCache = new Map();

/**
 * Converts raw RGBA pixel data into a standard uncompressed DIB-based ICO file.
 * Unlike PNG-based ICOs (which many viewers fail to read for sizes under 256x256), 
 * this uncompressed format is 100% universally readable by all OS/IDEs.
 *
 * @param {Buffer} rawBuffer - Raw RGBA pixel buffer generated by sharp
 * @param {number} width - Target width
 * @param {number} height - Target height
 * @returns {Buffer} The fully valid, strictly standard .ico file buffer
 */
function createIcoFromRawPixels(rawBuffer, width, height) {
  const w = width >= 256 ? 0 : width;
  const h = height >= 256 ? 0 : height;

  const xorSize = width * height * 4; 
  const andRowBytes = Math.ceil(width / 32) * 4; 
  const andSize = andRowBytes * height;
  const dibSize = 40 + xorSize + andSize;
  
  const headerLength = 22;
  const buffer = Buffer.alloc(headerLength + dibSize);
  
  // ICONDIR
  buffer.writeUInt16LE(0, 0); 
  buffer.writeUInt16LE(1, 2); 
  buffer.writeUInt16LE(1, 4); 

  // ICONDIRENTRY
  buffer.writeUInt8(w, 6);    
  buffer.writeUInt8(h, 7);    
  buffer.writeUInt8(0, 8);    
  buffer.writeUInt8(0, 9);    
  buffer.writeUInt16LE(1, 10); 
  buffer.writeUInt16LE(32, 12); 
  buffer.writeUInt32LE(dibSize, 14); 
  buffer.writeUInt32LE(headerLength, 18); 

  // BITMAPINFOHEADER
  const dibOffset = headerLength;
  buffer.writeUInt32LE(40, dibOffset + 0); 
  buffer.writeInt32LE(width, dibOffset + 4); 
  buffer.writeInt32LE(height * 2, dibOffset + 8); 
  buffer.writeUInt16LE(1, dibOffset + 12); 
  buffer.writeUInt16LE(32, dibOffset + 14); 
  buffer.writeUInt32LE(0, dibOffset + 16); 
  buffer.writeUInt32LE(xorSize + andSize, dibOffset + 20); 
  buffer.writeInt32LE(0, dibOffset + 24); 
  buffer.writeInt32LE(0, dibOffset + 28); 
  buffer.writeUInt32LE(0, dibOffset + 32); 
  buffer.writeUInt32LE(0, dibOffset + 36); 

  // XOR Mask (Pixels) - Written Bottom-Up in BGRA order for Windows
  const xorOffset = dibOffset + 40;
  for (let y = 0; y < height; y++) {
    const srcRowStart = y * width * 4;
    const destRowStart = xorOffset + (height - 1 - y) * width * 4;

    for (let x = 0; x < width; x++) {
      const srcOffset = srcRowStart + x * 4;
      const destOffset = destRowStart + x * 4;

      buffer[destOffset + 0] = rawBuffer[srcOffset + 2]; // Blue
      buffer[destOffset + 1] = rawBuffer[srcOffset + 1]; // Green
      buffer[destOffset + 2] = rawBuffer[srcOffset + 0]; // Red
      buffer[destOffset + 3] = rawBuffer[srcOffset + 3]; // Alpha
    }
  }

  return buffer;
}

/**
 * Modifies raw SVG markup to inject explicit dimensions and minify whitespace.
 * (For future scaling: consider replacing this with the 'svgo' package for deeper AST optimization).
 * * @param {Buffer} contentBuffer - The raw file buffer of the SVG
 * @param {number|null} width - Target width
 * @param {number|null} height - Target height
 * @returns {Buffer} The modified SVG string as a Buffer
 */
function transformSvgString(contentBuffer, width, height) {
  let svgStr = contentBuffer.toString("utf-8");
  
  if (width || height) {
    svgStr = svgStr.replace(/<svg([^>]+)>/i, (match, attributes) => {
      let newAttributes = attributes.replace(/\s*(?:width|height)="[^"]*"/gi, "");
      if (width) newAttributes += ` width="${width}"`;
      if (height) newAttributes += ` height="${height}"`;
      return `<svg ${newAttributes.trim()}>`;
    });
  }

  // Basic minification
  svgStr = svgStr.replace(/<!--[\s\S]*?-->/g, ""); // Remove comments
  svgStr = svgStr.replace(/[\r\n\t]+/g, " ");      // Remove line breaks/tabs
  svgStr = svgStr.replace(/>\s+</g, "><");         // Remove space between tags
  svgStr = svgStr.replace(/\s{2,}/g, " ");         // Collapse multiple spaces
  
  return Buffer.from(svgStr.trim());
}

/**
 * vitePluginDynamicSvg
 * * Intercepts SVG imports containing specific query parameters and processes them 
 * in-memory using `sharp`. Caches results to optimize build/dev server performance.
 *
 * Supported Query Parameters:
 * - `w` {number}: Target width in pixels.
 * - `h` {number}: Target height in pixels.
 * - `format` {string}: Output format (`svg` [default], `png`, `jpeg`, `jpg`, `webp`, `ico`).
 * - `as` {string}: Semantic alias for common assets (e.g., `favicon`, `apple-touch-icon`, `192x192`).
 *
 * Resizing Behaviors:
 * 1. Proportional Scaling: If ONLY `w` or ONLY `h` is provided (e.g., `?w=200`), 
 * the image will scale maintaining its original aspect ratio.
 * 2. Strict Bounding Box: If BOTH `w` and `h` are provided (e.g., `?w=50&h=50`),
 * the image will be scaled to fit entirely within that box without cropping, 
 * and any remaining space will be filled with a transparent padding.
 *
 * @example
 * // 1. Standard SVG Resize (Outputs optimized SVG string)
 * import logo from './logo.svg?w=200';
 * * // 2. Rasterization (Converts SVG to raster formats)
 * import heroWebp from './hero.svg?w=800&format=webp';
 * import thumbnailJpg from './thumb.svg?w=300&h=300&format=jpeg'; // Padded in 300x300 box
 *
 * // 3. Favicons & Manifest Icons
 * import appleIcon from './logo.svg?as=apple-touch-icon'; // Auto 180x180 png
 * import pwaIcon from './logo.svg?as=192x192';            // Auto 192x192 png
 * import faviconIco from './icon.svg?as=favicon&w=32&format=ico'; // True 32x32 uncompressed ICO
 *
 * @returns {import('vite').Plugin} The configured Vite plugin object.
 */
export default function vitePluginDynamicSvg() {
  return {
    name: 'vite-plugin-dynamic-svg',
    enforce: 'pre', 

    async load(id) {
      if (!id.includes('.svg?')) {
        return null; 
      }

      const [filePath, queryString] = id.split('?');
      const query = new URLSearchParams(queryString);
      
      const as = query.get("as");
      const queryFormat = query.get("format");
      let width = query.get("w") ? parseInt(query.get("w"), 10) : null;
      let height = query.get("h") ? parseInt(query.get("h"), 10) : null;

      // Determine format and dimensions based on 'as' aliases
      const isPredefinedAlias = as && (as.includes("favicon") || as.includes("apple") || as.includes("chrome"));
      let format = queryFormat || (isPredefinedAlias ? "png" : "svg");

      const sizeMatch = as ? as.match(/(\d+)x(\d+)/) : null;
      if (sizeMatch && !width && !height) {
        width = parseInt(sizeMatch[1], 10);
        height = parseInt(sizeMatch[2], 10);
      } else if (as && as.includes("apple-touch-icon") && !width) {
        width = 180;
        height = 180;
      } else if (as && as.includes("favicon") && !width) {
        width = 32;
        height = 32;
      }

      const hasExplicitArgs = query.has("w") || query.has("h") || query.has("format");

      // Ignore normal SVG imports that just happen to have unrelated query params (like ?url)
      if (!isPredefinedAlias && !hasExplicitArgs) {
        return null; 
      }

      // HMR Cache Busting: Append file's last modified time to cache key
      // Ensures dev server updates instantly when you edit and save the SVG file.
      let fileStat;
      try {
        fileStat = await fs.stat(filePath);
      } catch (err) {
        return null; // File doesn't exist, let Vite throw the standard error
      }
      const cacheKey = `${filePath}?${queryString}::${fileStat.mtimeMs}`;

      let finalBuffer;

      if (transformationCache.has(cacheKey)) {
        finalBuffer = transformationCache.get(cacheKey);
      } else {
        const content = await fs.readFile(filePath);

        if (format === "svg") {
          finalBuffer = transformSvgString(content, width, height);
        } else {
          // Rasterize using sharp
          let pipeline = sharp(content);
          
          if (width || height) {
            const resizeOptions = {};
            if (width) resizeOptions.width = width;
            if (height) resizeOptions.height = height;

            if (width && height) {
              resizeOptions.fit = 'contain';
              resizeOptions.background = { r: 0, g: 0, b: 0, alpha: 0 };
            }
            pipeline = pipeline.resize(resizeOptions);
          }
          
          if (format === "webp") {
            finalBuffer = await pipeline.webp().toBuffer();
          } else if (format === "jpeg" || format === "jpg") {
            finalBuffer = await pipeline.jpeg().toBuffer();
          } else if (format === "ico") {
            pipeline = pipeline.ensureAlpha().raw();
            const { data: rawBuffer, info } = await pipeline.toBuffer({ resolveWithObject: true });
            finalBuffer = createIcoFromRawPixels(rawBuffer, info.width, info.height);
          } else {
            finalBuffer = await pipeline.png().toBuffer();
          }
        }

        transformationCache.set(cacheKey, finalBuffer);
      }

      // Output to Vite's build pipeline
      const baseFileName = as ? as : path.basename(filePath, '.svg');
      const referenceId = this.emitFile({
        type: 'asset',
        name: `${baseFileName}.${format}`,
        source: finalBuffer
      });

      return `export default import.meta.ROLLUP_FILE_URL_${referenceId};`;
    }
  };
}